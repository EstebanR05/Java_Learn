package com.esteban.calculadora;

public class Aritmetic {

    public static Integer sum(Integer num, Integer num2) {
        return num + num2;
    }

    public static Integer res(Integer num, Integer num2) {
        return num - num2;
    }

    public static Integer multiplication(Integer num, Integer num2) {
        return num * num2;
    }
    
    public static Integer div(Integer num, Integer num2) {
        return num / num2;
    }
}
